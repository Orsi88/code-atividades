import math

# a)
graus = 45

seno = math.sin(math.radians(graus))
cosseno = math.cos(math.radians(graus))
tangente = math.tan(math.radians(graus))

print('{}°:'.format(graus))
print('SENO: {}'.format(seno))
print('COSSENO: {}'.format(cosseno))
print('TANGENTE: {}'.format(tangente))

